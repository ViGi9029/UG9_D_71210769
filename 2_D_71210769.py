#menghitung rata-rata uang saku
#dengan inputan
pertama = int(input("Uang saku anak pertama : "))
kedua = int(input("Uang saku anak kedua : "))
ketiga = int(input("Uang saku anak ketiga : "))

Rata = (pertama+kedua+ketiga)/3

print("Rata-rata dari",str(pertama)+"+"+str(kedua)+"+"+str(ketiga),"adalah", Rata)
